// Utility-Funktion für Legal-Übersetzungen
export const getLegalTranslation = (language, key, fallback = '') => {
  const translations = LEGAL_TRANSLATIONS[language] || LEGAL_TRANSLATIONS.en;
  
  // Unterstützung für verschachtelte Keys
  const keys = key.split('.');
  let value = translations;
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return fallback || key;
    }
  }
  
  return value || fallback || key;
};

export default getLegalTranslation;