// Utility-Funktionen f√ºr √úbersetzungen
export const getTranslation = (language, key, fallback = '') => {
  const translations = TRANSLATIONS[language] || TRANSLATIONS.en;

  // Unterst√ºtzung f√ºr verschachtelte Keys (z.B. "topics.Geography")
  const keys = key.split('.');
  let value = translations;

  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return fallback || key;
    }
  }

  return value || fallback || key;
};

// √úberpr√ºfung ob eine Sprache verf√ºgbar ist
export const isLanguageAvailable = (languageCode) => {
  return AVAILABLE_LANGUAGES.some(lang => lang.code === languageCode);
};

// Fallback-Sprache ermitteln
export const getFallbackLanguage = () => {
  const browserLang = navigator.language.substring(0, 2);
  return isLanguageAvailable(browserLang) ? browserLang : 'en';
};

